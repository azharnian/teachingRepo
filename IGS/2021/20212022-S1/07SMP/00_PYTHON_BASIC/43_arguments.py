
# arguments, parameters, return, call back function, def

def tambah(a,b): # a dan b merupakan argument
	return a+b

def pangkat_2(a): # a adalah argument
	return a**2

def buat_pempek(sagu, ikan, garam, sasa):
	return sagu+ikan+garam+sasa

print(tambah(20, 30)) # call back function, 20 & 30 -> parameters
print(pangkat_2(4))